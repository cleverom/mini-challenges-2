function morse(text) {
  var phrase = [];
  text.trim();
  var words = text.split("   ");
  for (var i = 0; i < words.length; i++) {
    var letters = words[i].split(" ");
    for (var j = 0; j < letters.length; j++) {
      letters[j] = MORSE_CODE[letters[j]];
    }
    phrase.push(letters.join(""));
  }
  return phrase.join(" ").trim();
}

